#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)str;
	while (str[i] && str[i] != c)
		i++;
	if (str[i] == c)
		return (s + i);
	return (NULL);
}
