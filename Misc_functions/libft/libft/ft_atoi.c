int		ft_atoi(const char *str)
{
	int					neg;
	int					i;
	unsigned long long	n;

	neg = 1;
	n = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n *= 10;
		n += str[i] - '0';
		i++;
	}
	if (n > 9223372036854775807)
		return (neg == -1 ? 0 : -1);
	return ((int)n * neg);
}
